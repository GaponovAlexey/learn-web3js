  function numberWithSpaces(x) {
    var parts = x.toString().split('.')
    parts[0] = parts[0].replace(/\B(?=(\d{3})+(?!\d))/g, ' ')
    return parts.join('.')
  }

  function numberToDecimals(number, decimals) {
    let stringNumber
    number = number * 1
    decimals = decimals * 1

    if (number < 1) {
      stringNumber = number * 10 ** decimals
    } else {
      let decimalsLength = countDecimals(number)

      let zeros = decimals - decimalsLength

      stringNumber = number.toString(10)
      for (let i = 0; i < zeros; i++) {
        stringNumber = stringNumber + '0'
      }

      stringNumber = stringNumber.replace('.', '')
    }

    return stringNumber
  }

